let addition = 4 + 5;
let subtraction = 10 - 3;
let multiplication = 7 * 6;
let division = 20 / 4;
let remainder = 10 % 3; // Not used but demonstrates modulus operator
let exponentiation = 2 ** 3;
let score = 102
let bonus = 25
let totalScore = score;

totalScore += bonus; // totalScore = totalScore + bonus

console.log("Addition:", addition);
console.log("Subtraction:", subtraction);
console.log("Multiplication:", multiplication);
console.log("Division:", division);
console.log("Total Score after bonus:", totalScore);
// Note: The 'remainder' variable is declared but not used in any operation.
console.log("Remainder (not used):", remainder);
console.log("Exponentiation:", exponentiation);


// Comparison Operators
let isEqual = (5 == '5');
let isStrictEqual = (5 === '5');
let isNotEqual = (5 != '5');
let isGreaterThan = (10 > 5);
let isLessThanOrEqual = (5 <= 5);

console.log("Is Equal (==):", isEqual);
console.log("Is Strictly Equal (===):", isStrictEqual);
console.log("Is Not Equal (!=):", isNotEqual);
console.log("Is Greater Than (>):", isGreaterThan);
console.log("Is Less Than or Equal (<=):", isLessThanOrEqual);

// Logical Operators
let andOperation = (true && false);
let orOperation = (true || false);
let notOperation = !true;

console.log("AND Operation (true && false):", andOperation);
console.log("OR Operation (true || false):", orOperation);
console.log("NOT Operation (!true):", notOperation);

// Increment and Decrement Operators
let counter = 10;
counter++; // counter = counter + 1
counter--; // counter = counter - 1
console.log("Counter after increment and decrement:", counter);


